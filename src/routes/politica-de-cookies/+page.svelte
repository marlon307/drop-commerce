  <h2 class="my-4 block text-xl font-medium">2. O que são Cookies?</h2>
  <p>
    Cookies são pequenos arquivos de texto que um site salva no seu computador
    ou dispositivo móvel quando você o visita. Eles armazenam informações sobre
    a sua navegação e preferências, como idioma, login e itens do carrinho de
    compras.
  </p>
  <h2 class="my-4 block text-xl font-medium">3. Tipos de Cookies que Usamos</h2>
  <h2 class="my-4 block text-xl font-medium">3.1 Cookies Essenciais:</h2>
  <p>
    São essenciais para o funcionamento do Site e permitem que você navegue e
    utilize seus recursos, como adicionar produtos ao carrinho de compras e
    finalizar a compra. Sem esses cookies, algumas funcionalidades podem não
    funcionar corretamente.
  </p>
  <h2 class="my-4 block text-xl font-medium">3.2 Cookies de Desempenho:</h2>
  <p>
    Coletam informações sobre como você utiliza o Site, como as páginas que você
    visita e os links em que clica. Essas informações são utilizadas para
    melhorar a experiência do usuário e o desempenho do Site.
  </p>
  <h2 class="my-4 block text-xl font-medium">3.3 Cookies de Funcionalidade:</h2>
  <p>
    Permitem que o Site personalize sua experiência, como lembrar seu idioma
    preferido ou os produtos que você visualizou recentemente.
  </p>
  <h2 class="my-4 block text-xl font-medium">3.4 Cookies de Publicidade:</h2>
  <p>
    São utilizados para mostrar anúncios relevantes para você, tanto dentro como
    fora do Site. Eles rastreiam sua navegação em diferentes sites e coletam
    informações sobre seus interesses.
  </p>
  <h2 class="my-4 block text-xl font-medium">4. Como Gerenciar Cookies</h2>
  <p>
    Você pode gerenciar suas preferências de cookies a qualquer momento através
    das configurações do seu navegador. É importante lembrar que desabilitar os
    cookies pode afetar o funcionamento do Site.
  </p>
