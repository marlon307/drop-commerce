  </p>
  <span class="block">POLÍTICA DE TROCAS E DEVOLUÇÕES</span>
  <span>Condições gerais</span>
  <p>
    Utilizamos tecnologia de ponta para a fabricação e distribuição dos nossos
    produtos, primando pela qualidade e satisfação dos nossos clientes. Pelo
    respeito e para que seja mantida a credibilidade conquistada junto aos seus
    consumidores, a empresa criou a política de troca, devolução e
    arrependimento de acordo com o Código de Defesa do Consumidor.
  </p>
  <p>
    Art. 49 do CDC: “O consumidor pode desistir do contrato, no prazo de 7 dias
    a contar do ato de recebimento do produto ou serviço, sempre que a
    contratação de fornecimento de produtos e serviços ocorrer fora do
    estabelecimento comercial, especialmente por telefone ou a domicílio”.
  </p>
  <span class="block">POLÍTICA DE TROCA, DEVOLUÇÃO E ARREPENDIMENTO</span>
  <p>
    A desistência ou o arrependimento ocorre somente quando a compra é efetuada
    fora do estabelecimento comercial. Neste caso, o consumidor deverá
    manifestar seu interesse em desistir da compra no prazo de até 7 (sete) dias
    corridos a contar da data do recebimento do produto; Caso o prazo não seja
    respeitado, ficará sob pena de perda do direito de desistência.
  </p>
  <p>
    Para devolução do produto, é necessário que este esteja em ótimas condições,
    sem indícios de uso inadequado ou avaria física (ex.: amassados, arranhões,
    oxidação, descaracterização do item adquirido, componente queimado,
    utilização de acessório inautêntico). Lembrando: É obrigatório a devolução
    do produto com a sua embalagem original. Só serão aceitas devoluções, por
    quaisquer motivos, quando a embalagem do produto é devolvida sem nenhuma
    avaria/dano.
  </p>
  <p>
    A troca ficará condicionada a disponibilidade do produto em estoque. Caso
    não haja o produto em estoque, você poderá optar por uma das seguintes
    alternativas:
  </p>
  <p class="ml-6">
    - Escolher outro produto, pagando ou recebendo de volta a diferença de
    valores, se houver; ou receber a restituição do valor pago. Além disso o
    cliente poderá optar pelo crédito da loja, utilizando o mesmo em novas
    compras.
  </p>
  <p>
    A desistência deve ser informada através do e-mail contato e nosso time irá
    te orientar como proceder.
  </p>
  <p>
    Após o recebimento do(s) respectivo(s) produto(s) em nosso endereço iremos
    analisar, se este estiver dentro das normas de devolução retornaremos o
    contato para podermos solucionar o acontecido.
  </p>
  <p>
    O valor restituído refere-se ao valor pago sobre o(s) produto(s)
    devolvido(s), ou seja, levando em consideração eventuais descontos e/ou
    promoções vigentes no site.
  </p>
  <p>
    Exemplo: Se você comprou a promoção “Compre três e ganhe dois” e pagou pelos
    produtos R$479,70 e dentro dos 7 dias opta por desistir da promoção, o
    cálculo feito é:
  </p>
  <p>Valor pago: R$ 479,70 por 3 produtos</p>
  <p>Valor unitário do produto: R$159,90</p>
