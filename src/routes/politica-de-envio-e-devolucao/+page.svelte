<section class="mx-auto mt-4 max-w-3xl space-y-4 px-4 text-lg text-neutral-200">
  <h1 class="mb-4 text-4xl font-bold">Política de Envio e Devolução Biguti</h1>
  <h2 class="text-xl font-medium">1. Prazos de Entrega</h2>
  <p>
    O prazo de entrega varia de acordo com a região do Brasil, o método de envio
    escolhido no momento da compra e a disponibilidade do produto em estoque.
  </p>
  <h2 class="my-4 block text-xl font-medium">
    1.1. Cálculo do Prazo de Entrega
  </h2>
  <p>
    O prazo de entrega começa a ser contado a partir da confirmação do
    pagamento. Para calcular o prazo, utilize a ferramenta de simulação
    disponível no site da Biguti ou consulte o FAQ.
  </p>
  <h2 class="my-4 block text-xl font-medium">1.2. Áreas de Abrangência</h2>
  <p>A Biguti entrega em todo o território nacional.</p>
  <h2 class="my-4 block text-xl font-medium">1.3. Métodos de Envio</h2>
  <span class="block">A Biguti oferece diversas opções de envio, como:</span>
  <ul class="ml-8 *:list-disc">
    <li><b>PAC:</b> modalidade econômica com entrega em até 10 dias úteis.</li>
    <li><b>SEDEX:</b> modalidade expressa com entrega em até 3 dias úteis.</li>
    <!-- <li>Retirada na loja: disponível para produtos selecionados.</li> -->
  </ul>
  <h2 class="my-4 block text-xl font-medium">2. Custos de Frete</h2>
  <p>
    O custo do frete é calculado de acordo com o peso e volume do produto, o CEP
    de destino e o método de envio escolhido. O valor do frete é informado no
    carrinho de compras antes da finalização da compra.
  </p>
  <h2 class="my-4 block text-xl font-medium">2.1. Frete Grátis</h2>
  <p>
    A Biguti oferece frete grátis para compras acima de um determinado valor,
    que pode variar de acordo com a região do Brasil.
  </p>
  <h2 class="my-4 block text-xl font-medium">3. Rastreamento do Pedido</h2>
  <p>
    Após a confirmação do pagamento, o cliente receberá um e-mail com o código
    de rastreamento do pedido. O rastreamento pode ser feito através do site da
    transportadora ou do site da Biguti.
  </p>
  <h2 class="my-4 block text-xl font-medium">4. Devolução e Troca</h2>
  <p>
    O cliente tem o direito de devolver ou trocar o produto adquirido em até 7
    dias corridos após o recebimento.
  </p>
  <h2 class="my-4 block text-xl font-medium">
    4.1. Condições para Devolução ou Troca
  </h2>
  <p>
    O produto deve estar em sua embalagem original, sem uso, com todos os
    acessórios e etiquetas.
  </p>
  <h2 class="my-4 block text-xl font-medium">
    4.2. Como Fazer a Devolução ou Troca
  </h2>
  <p>
    Para solicitar a devolução ou troca, o cliente deve entrar em contato com a
    Central de Atendimento da Biguti através do telefone, e-mail ou chat online.
  </p>
  <h2 class="my-4 block text-xl font-medium">4.3. Prazo para Reembolso</h2>
  <p>
    O reembolso do valor pago será realizado em até 10 dias úteis após o
    recebimento do produto na Biguti.
  </p>
  <h2 class="my-4 block text-xl font-medium">4.4. Troca por Outro Produto</h2>
  <p>
    Em caso de troca por outro produto, o cliente deverá arcar com o custo do
    frete de devolução do produto original e o custo do frete do novo produto.
  </p>
  <h2 class="my-4 block text-xl font-medium">
    5. Exceções à Política de Devolução e Troca
  </h2>
  <p>Não é possível devolver ou trocar produtos:</p>
  <ul class="ml-8 *:list-disc">
    <li>Em promoção ou liquidação.</li>
    <li>Personalizados.</li>
    <li>Usados ou danificados.</li>
    <li>Sem a embalagem original.</li>
    <li>Sem as etiquetas.</li>
  </ul>
